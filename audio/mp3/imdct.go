func imdctWin(in []float32, blockType int) []float32 {
	out := make([]float32, 36)
	if blockType == 2 {
		N := 12
		for i := 0; i < 3; i++ {
			for p := 0; p < N; p++ {
				sum := float32(0.0)
				for m := 0; m < N/2; m++ {
					sum += in[i+3*m] * cos_N12[m][p]
				}
				out[6*i+p+6] += sum * imdctWinData[blockType][p]
			}
		}
		return out
	}
	N := 36
	for p := 0; p < N; p++ {
		sum := float32(0.0)
		for m := 0; m < N/2; m++ {
			sum += in[m] * cos_N36[m][p]
		}
		out[p] = sum * imdctWinData[blockType][p]
	}
	return out
}
